public class Fans {

    private String mainFriendName;

    private String otherFriendName;

    private int fansOrNot;

    public String getMainFriendName() {
        return mainFriendName;
    }

    public void setMainFriendName(String mainFriendName) {
        this.mainFriendName = mainFriendName;
    }

    public String getOtherFriendName() {
        return otherFriendName;
    }

    public void setOtherFriendName(String otherFriendName) {
        this.otherFriendName = otherFriendName;
    }

    public int getFansOrNot() {
        return fansOrNot;
    }

    public void setFansOrNot(int fansOrNot) {
        this.fansOrNot = fansOrNot;
    }
}
